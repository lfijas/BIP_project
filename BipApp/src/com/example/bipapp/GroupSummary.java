package com.example.bipapp;

public class GroupSummary {
	
	private double confections;
	private double Dairy;
	private double Fruit;
	private double Grains;
	private double Meat;
	private double Vegetable;
	private double Water;
	private double Miscellaneous;
	
	public double getConfections() {
		return confections;
	}
	public void setConfections(double confections) {
		this.confections = confections;
	}
	public double getDairy() {
		return Dairy;
	}
	public void setDairy(double dairy) {
		Dairy = dairy;
	}
	public double getFruit() {
		return Fruit;
	}
	public void setFruit(double fruit) {
		Fruit = fruit;
	}
	public double getGrains() {
		return Grains;
	}
	public void setGrains(double grains) {
		Grains = grains;
	}
	public double getMeat() {
		return Meat;
	}
	public void setMeat(double meat) {
		Meat = meat;
	}
	public double getVegetable() {
		return Vegetable;
	}
	public void setVegetable(double vegetable) {
		Vegetable = vegetable;
	}
	public double getWater() {
		return Water;
	}
	public void setWater(double water) {
		Water = water;
	}
	public double getMiscellaneous() {
		return Miscellaneous;
	}
	public void setMiscellaneous(double miscellaneous) {
		Miscellaneous = miscellaneous;
	}
	
	
	
}
